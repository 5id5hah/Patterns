import java.util.regex.Pattern;

public class Solid_Square {
        public static void main(String[] args) {
            Pattern(5);
        }
           public static void Pattern(int n){
            for (int i = 1; i < n; i++) {
                for (int j = 1; j < n; j++) {
                    System.out.print(" * ");
                }
                System.out.println(" ");

            }
        }
    }


